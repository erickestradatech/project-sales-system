package modelo;

public class Ventas {

    private int codart;
    private String nomcli;
    private int meses;

    public int getCodart() {
        return codart;
    }

    public void setCodart(int codart) {
        this.codart = codart;
    }

    public String getNomcli() {
        return nomcli;
    }

    public void setNomcli(String nomcli) {
        this.nomcli = nomcli;
    }

    public int getMeses() {
        return meses;
    }

    public void setMeses(int meses) {
        this.meses = meses;
    }

}
